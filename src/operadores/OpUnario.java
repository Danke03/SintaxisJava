package operadores;

public class OpUnario { //incrementa o decrementa 1, solo en enteros, puede ser prefijada o posfijada
	public static void main (String[]args) {
		
		int a = 100;
		int b = 50;
		
		System.out.println("a vale: "+ a);
		System.out.println("b vale: "+ b);
		
		a++;
		b--;
		
		System.out.println("a vale: "+ a);
		System.out.println("b vale: "+ b);
	}

}
